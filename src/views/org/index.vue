<script lang="ts">
import { Component, Vue } from 'vue-property-decorator'
import Treeselect from 'vue-treeselect'
import "@riophae/vue-treeselect/dist/vue-treeselect.css";
import {IPageParam, ISearchFilter, Organizations} from "@/api/types";
import {defaultOrg} from "@/api/defaultTypes";
import {defaultPageParam} from "@/utils/utils";
import {addDept, listOrgs, updateDept} from "@/api/app/orgs";
import {Form} from "element-ui";


  @Component({
    name: 'Organizations',
    components: { Treeselect }
  })
export default class extends Vue{
      // 遮罩层
    private loading= true
     // 显示搜索条件
    private showSearch= true
     // 表格树数据
    private deptList= []
     // 部门树选项
    private deptOptions= []
     // 弹出层标题
    private title= ""
     // 是否显示弹出层
    private open= false
     // 状态数据字典
    private statusOptions= [{
      dictValue:'0',
      dictLable:'正常',
    },
      {
        dictValue:'1',
        dictLable:'停用',
      }
    ]
    private opt: Map<string, Array<string>> = new Map()
    private listQuery : IPageParam =  defaultPageParam
     // 查询参数
    private queryParams:Organizations = defaultOrg
      // 表单参数
    private  form: Organizations= defaultOrg
      // 表单校验
    private   rules= {
        parentId: [
          { required: true, message: "上级部门不能为空", trigger: "blur" }
        ],
        deptName: [
          { required: true, message: "部门名称不能为空", trigger: "blur" }
        ],
        orderNum: [
          { required: true, message: "显示排序不能为空", trigger: "blur" }
        ],
        email: [
          {
            type: "email",
            message: "'请输入正确的邮箱地址",
            trigger: ["blur", "change"]
          }
        ],
        phone: [
          {
            pattern: /^1[3|4|5|6|7|8|9][0-9]\d{8}$/,
            message: "请输入正确的手机号码",
            trigger: "blur"
          }
        ]
      }


  created() {
    this.getList();

  }

    /** 查询部门列表 */
   private async getList() {
      this.loading = true;
      const {data} = await listOrgs(this.listQuery)
      this.deptList = data.items
      //this.total = data.total
      // Just to simulate the time of the request
      setTimeout(() => {
        this.loading = false
      }, 0.5 * 1000)
    }
    /** 转换部门数据结构 */
    private normalizer(node: any) {
      if (node.children && !node.children.length) {
        delete node.children;
      }
      return {
        id: node.deptId,
        label: node.deptName,
        children: node.children
      };
    }

    // 取消按钮
   private cancel() {
      this.open = false;
      this.reset();
    }
    // 表单重置
   private reset() {
      // this.form = {
      //   deptId: undefined,
      //   parentId: undefined,
      //   deptName: undefined,
      //   orderNum: undefined,
      //   leader: undefined,
      //   phone: undefined,
      //   email: undefined,
      //   status: "0"
      // };
      this.form = defaultOrg
      this.resetForm("form")
    }
    /** 搜索按钮操作 */
  private  handleQuery() {
      this.getList();
    }
    /** 重置按钮操作 */
    private  resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    }
    /** 新增按钮操作 */
    private handleAdd(row: any) {
      this.reset();
      if (row != undefined) {
        this.form.parentId = row.deptId;
      }
      this.open = true;
      this.title = "添加部门";
      // listDept().then(response => {
	    //     this.deptOptions = this.handleTree(response.data, "deptId");
      // });
    }
    /** 修改按钮操作 */
    private handleUpdate(row) {
      this.reset();
      // getDept(row.deptId).then(response => {
      //   this.form = response.data;
      //   this.open = true;
      //   this.title = "修改部门";
      // });
      // listDeptExcludeChild(row.deptId).then(response => {
	    //     this.deptOptions = this.handleTree(response.data, "deptId");
      // });
    }
    /** 提交按钮 */
   private submitForm() {
      (this.$refs.form as Form).validate(valid => {
        if (valid) {
          if (this.form.id !== undefined) {
            updateDept(this.form).then(response => {
              this.msgSuccess("修改成功");
              this.open = false
              this.getList();
            });
          } else {
            addDept(this.form).then(response => {
              this.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    }
    /** 删除按钮操作 */
   private  handleDelete(row: any) {
      this.$confirm('是否确认删除名称为"' + row.deptName + '"的数据项?', "警告", {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning"
        }).then(function() {
          return delDept(row.deptId)
        }).then(() => {
          this.getList();
          this.msgSuccess("删除成功");
        })
    }

}
</script>
